// Language names mapping
const langNames = {
    'zh-CN': '简体中文',
    'zh-TW': '繁體中文',
    'en': 'English'
};

// Language switching logic
function setLanguage(lang) {
    document.documentElement.lang = lang;
    localStorage.setItem('iec-lang', lang);

    // Update all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Update lang dropdown toggle text
    const langCurrent = document.querySelector('.lang-current');
    if (langCurrent) {
        langCurrent.textContent = langNames[lang] || lang;
    }

    // Update lang dropdown options
    document.querySelectorAll('.lang-option').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Close dropdown after selection
    const langDropdown = document.querySelector('.lang-dropdown');
    if (langDropdown) {
        langDropdown.classList.remove('open');
    }

    // Sync overlay language buttons
    document.querySelectorAll('.menu-lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Update font family based on language
    if (lang === 'zh-TW') {
        document.body.style.fontFamily = "'Inter', 'Noto Sans TC', -apple-system, sans-serif";
    } else if (lang === 'zh-CN') {
        document.body.style.fontFamily = "'Inter', 'Noto Sans SC', -apple-system, sans-serif";
    } else {
        document.body.style.fontFamily = "'Inter', -apple-system, sans-serif";
    }
}

// Initialize language
function initLanguage() {
    const saved = localStorage.getItem('iec-lang');
    const browserLang = navigator.language;
    let lang = 'zh-CN'; // default

    if (saved && translations[saved]) {
        lang = saved;
    } else if (browserLang.startsWith('zh-TW') || browserLang.startsWith('zh-Hant')) {
        lang = 'zh-TW';
    } else if (browserLang.startsWith('en')) {
        lang = 'en';
    }

    setLanguage(lang);
}

// Dropdown functionality
document.addEventListener('DOMContentLoaded', () => {
    const langToggle = document.getElementById('langToggle');
    const langDropdown = document.querySelector('.lang-dropdown');
    const langOptions = document.querySelectorAll('.lang-option');

    // Toggle dropdown
    if (langToggle) {
        langToggle.addEventListener('click', (e) => {
            e.stopPropagation();
            langDropdown.classList.toggle('open');
        });
    }

    // Bind language options
    langOptions.forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.dataset.lang);
        });
    });

    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
        if (langDropdown && !langDropdown.contains(e.target)) {
            langDropdown.classList.remove('open');
        }
    });

    // Auto-init language
    initLanguage();
});
